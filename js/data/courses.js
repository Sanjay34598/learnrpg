/**
 * courses.js
 * ─────────────────────────────────────────────────────────────────────────
 * Single source of truth for all course and quest data.
 *
 * Structure:
 *   COURSES  — global object keyed by courseId
 *     .name      — display name
 *     .mark      — 2–3 letter abbreviation shown in the course list
 *     .tagline   — short description
 *     .quests[]  — ordered array of quest objects
 *
 * Quest object fields:
 *   id         — unique string identifier (used for localStorage keys)
 *   title      — display title shown in mission list and arena header
 *   subtitle   — one-line description shown under the title
 *   type       — "choice" | "code" | "think"
 *   difficulty — label string (Starter / Apprentice / Adept / Skilled / Expert)
 *   skill      — skill being practised
 *   reward     — loot item name shown in codex
 *   icon       — single char for badge
 *   pos        — [x%, y%] position on the quest map
 *   summary    — one-sentence description
 *   prompt     — the actual task shown to the player
 *   code       — code snippet shown in the terminal
 *   ─ For type "choice":
 *       choices[]  — array of option strings
 *       answer     — index of correct choice
 *       output     — expected output shown when solved
 *   ─ For type "code":
 *       checks[]   — substrings that must appear in the player's submission
 *       output     — model answer shown when solved
 *   ─ For type "think":
 *       minLength  — minimum character count before accepting the answer
 *       output     — model answer shown when solved
 *   concept    — pedagogical explanation in the Mentor codex
 *   pattern    — mental model / heuristic
 *   example    — code example in the Pattern tab
 *   why        — short "why this works" revealed after solving
 */

